  // ============ MUSIC PROMPT GENERATOR (SUNO WORKFLOW) ============

  // Generate music prompt for Suno - Step 1 of the workflow
  router.post('/series/:seriesId/generate-music-prompt', async (req, res) => {
    try {
      const { seriesId } = req.params;
      const { theme, conflict, location, emotionalJourney, musicStyle, tempo, mood } = req.body;

      // Validate required fields
      if (!theme || !conflict || !musicStyle || !tempo || !mood) {
        return res.status(400).json({
          success: false,
          error: 'Missing required fields: theme, conflict, musicStyle, tempo, mood',
        });
      }

      const series = await storage.getSeries(seriesId);
      if (!series) {
        return res.status(404).json({ success: false, error: 'Series not found' });
      }

      const latestEpisode = await storage.getLatestEpisode(seriesId);
      const episodeNumber = (latestEpisode?.episodeNumber || 0) + 1;

      console.log('🎵 Generating Music Prompt for Suno...');
      const musicPrompt = await storyGenerator.generateMusicPrompt(
        series as any,
        episodeNumber,
        {
          theme,
          conflict,
          location: location || series.setting?.name || 'unknown location',
          emotionalJourney: emotionalJourney || 'struggle to triumph',
          musicStyle,
          tempo,
          mood,
        },
        latestEpisode?.synopsis,
      );

      // Save generated song to series for the Songs tab
      const existingSongs = (series as any).generatedSongs || [];
      const newSong = {
        episodeNumber,
        episodeTitle: musicPrompt.episodeContext?.title || `Episode ${episodeNumber}`,
        style: musicPrompt.sunoPrompt.style,
        lyrics: musicPrompt.sunoPrompt.lyrics,
        tags: musicPrompt.sunoPrompt.tags,
        synopsis: musicPrompt.episodeContext?.synopsis,
        createdAt: new Date().toISOString(),
      };

      await storage.updateSeries(seriesId, {
        generatedSongs: [...existingSongs, newSong],
      });

      res.json({
        success: true,
        data: {
          seriesId,
          seriesTitle: series.title,
          episodeNumber,
          musicPrompt,
        },
      });
    } catch (error: any) {
      console.error('Music prompt generation error:', error);
      res.status(500).json({
        success: false,
        error: error.message || 'Failed to generate music prompt',
      });
    }
  });


