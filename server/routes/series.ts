  // Generate video from episode
  router.post('/episodes/:episodeId/generate-video', async (req, res) => {
    try {
      const episode = await storage.getEpisode(req.params.episodeId);
      if (!episode) {
        return res.status(404).json({ success: false, error: 'Episode not found' });
      }

      const series = await storage.getSeries(episode.seriesId);
      if (!series) {
        return res.status(404).json({ success: false, error: 'Series not found' });
      }

      // Handle rap battles vs regular episodes differently
      const isRapBattle = episode.episodeType === 'rap_battle';
      let scenes: any[] = [];
      let scriptContent = '';

      if (isRapBattle && episode.battleData) {
        // For rap battles, use visualsJson for scenes and cleanLyrics for script
        const visualsJson = episode.battleData.visualsJson || [];
        const rounds = episode.battleData.rounds || [];

        // Performance action templates for each round - ensures characters are RAPPING not walking
        const getPerformanceAction = (roundIdx: number, characterName: string, isAntagonist: boolean): string => {
          const roundActions = [
            // Round 1: Antagonist opens with dominance
            isAntagonist
              ? `${characterName} RAPPING AGGRESSIVELY, standing atop elevated position, arms spread wide in dominance, face twisted with contempt, delivering opening verse with powerful hand gestures, chest puffed up, looking down at opponent`
              : `${characterName} RAPPING DEFIANTLY, stepping forward with fists clenched, pointing at opponent, chin raised, voice carrying power, building intensity`,
            // Round 2: Response with defiance
            isAntagonist
              ? `${characterName} RAPPING with controlled fury, pacing like a caged predator, calculated gestures, eyes locked on opponent, each word delivered with surgical precision`
              : `${characterName} RAPPING INTENSELY, stepping forward with explosive energy, pointing finger at opponent accusingly, body language showing rising confidence, voice booming`,
            // Round 3: Escalation - tension peaks
            isAntagonist
              ? `EXTREME CLOSE-UP of ${characterName} RAPPING WITH VISIBLE RAGE, composure cracking, grip tightening, veins visible, spitting bars with desperate intensity, sweat on brow`
              : `${characterName} RAPPING RELENTLESSLY, advancing toward opponent, jabbing finger rhythmically with each bar, crowd energy feeding their power, unstoppable momentum`,
            // Round 4: Finale - knockout
            isAntagonist
              ? `${characterName} RAPPING defensively, backing up, stumbling on words, dominance crumbling, fear in eyes as opponent approaches`
              : `${characterName} DELIVERING KNOCKOUT BARS, ascending triumphantly, arms raised in victory, opponent cowering, crowd explosion behind them, ultimate power moment`,
          ];
          return roundActions[Math.min(roundIdx, 3)] || roundActions[0];
        };

        // ALWAYS build scenes from rounds (primary source of truth for performer/role)
        // Use visualsJson only for supplementary visual metadata
        if (rounds.length > 0) {
          scenes = rounds.map((round: any, idx: number) => {
            // Get role directly from round data (authoritative source)
            const isAntagonist = round.role === 'antagonist';
            const characterName = round.character || (isAntagonist ? 'Antagonist' : 'Protagonist');
            const performanceAction = getPerformanceAction(idx, characterName, isAntagonist);

            // Get supplementary visual info from visualsJson if available
            const visualInfo = visualsJson[idx] || {};
            const camera = visualInfo.camera || round.cameraMovement || 'dynamic camera';
            const lighting = visualInfo.lighting || 'dramatic';
            const mood = visualInfo.mood || round.mood || 'intense';
            const environment = visualInfo.scene || round.scene || 'battle arena';

            // Combine performance action with environment
            const fullPrompt = `${performanceAction}. Environment: ${environment}. ${lighting} lighting, ${camera}. Mood: ${mood}. RAP BATTLE PERFORMANCE - character actively performing and rapping, not walking.`;

            return {
              sceneNumber: idx + 1,
              start: 0,
              end: 0,
              prompt: fullPrompt,
              cameraWork: camera,
              mood: mood,
              character: characterName,
              lyrics: round.lyrics || '',
            };
          });
        } else if (visualsJson.length > 0) {
          // Fallback to visualsJson if no rounds (shouldn't happen, but be safe)
          scenes = visualsJson.map((v: any, idx: number) => {
            const isAntagonist = v.section?.toLowerCase().includes('antagonist') || idx % 2 === 0;
            const characterName = v.character || (isAntagonist ? 'Antagonist' : 'Protagonist');
            const performanceAction = getPerformanceAction(idx, characterName, isAntagonist);

            const fullPrompt = `${performanceAction}. Environment: ${v.scene}. ${v.lighting} lighting, ${v.camera}. Mood: ${v.mood}. RAP BATTLE PERFORMANCE - character actively performing, not walking.`;

            return {
              sceneNumber: idx + 1,
              start: 0,
              end: 0,
              prompt: fullPrompt,
              cameraWork: v.camera || 'medium shot',
              mood: v.mood || 'intense',
              character: characterName,
              lyrics: '',
            };
          });
        }

        // For rap battles, use scene prompts as script content (NOT just lyrics)
        // This ensures the job-worker generates videos with the performance actions we specified
        scriptContent = scenes
          .map((s: any, idx: number) => {
            // Get lyrics from scene object (added above) or from rounds
            const lyrics = s.lyrics || rounds[idx]?.lyrics || '';

            // Only include lyrics section if we have actual lyrics
            const lyricsSection = lyrics.trim() ? `LYRICS:\n${lyrics}` : '';

            return `[SCENE ${idx + 1}]
VISUAL: ${s.prompt}
${lyricsSection}`.trim();
          })
          .join('\n\n---\n\n');

        // If no scene prompts, fall back to clean lyrics
        if (!scriptContent || scriptContent.length < 50) {
          scriptContent =
            episode.battleData.cleanLyrics || rounds.map((r: any) => r.lyrics).join('\n\n') || episode.synopsis;
        }

        // Log the prompts for debugging
        console.log(`🎤 Rap battle video: ${scenes.length} scenes with performance prompts`);
        scenes.forEach((s: any, i: number) => {
          console.log(`   Scene ${i + 1}: ${s.prompt.substring(0, 100)}...`);
        });
      } else {
        // Regular episode - use generatedScenes
        scenes = episode.generatedScenes || [];
        scriptContent = scenes.map((s: any) => s.prompt).join('\n\n');
      }

      // Validate we have content
      if (scenes.length === 0) {
        return res.status(400).json({
          success: false,
          error: 'No scenes found. Please ensure the episode has generated scenes or battle data.',
        });
      }

      if (!scriptContent || scriptContent.length < 10) {
        scriptContent = episode.synopsis || scenes.map((s: any) => s.prompt).join('\n\n');
      }

      // Check for music URL - required for video generation
      if (!episode.musicUrl) {
        return res.status(400).json({
          success: false,
          error: 'No music uploaded. Please upload music for this episode before generating video.',
        });
      }

      const characterIds = req.body.characterIds || [];

      // For rap battles, auto-add protagonist and antagonist character profiles
      if (isRapBattle && characterIds.length === 0) {
        if (series.protagonist?.characterProfileId) {
          characterIds.push(series.protagonist.characterProfileId);
        }
        if (series.antagonist?.characterProfileId) {
          characterIds.push(series.antagonist.characterProfileId);
        }
      }

      // Use video mode from episode or request, default to consistent if character profiles exist
      const mode = (episode as any).videoMode || (characterIds.length > 0 ? 'consistent' : 'veo');
      // Priority: request body > episode stored value > default to 9:16 for social media
      const selectedAspectRatio = req.body.aspectRatio || (episode as any).aspectRatio || '9:16';
      console.log(
        `🎬 Video generation: mode=${mode}, aspectRatio=${selectedAspectRatio}, characters=${characterIds.length}`,
      );

      // Parse post-processing options if provided
      let postProcessingOptions = null;
      if (req.body.postProcessing) {
        try {
          postProcessingOptions =
            typeof req.body.postProcessing === 'string' ? JSON.parse(req.body.postProcessing) : req.body.postProcessing;
          console.log(`🎬 Post-processing options:`, postProcessingOptions);
        } catch (e) {
          console.warn('Could not parse postProcessing options:', e);
        }
      }

      const jobData = insertJobSchema.parse({
        scriptName: `${series.title} - ${episode.title}`,
        scriptContent,
        mode,
        aspectRatio: selectedAspectRatio,
        musicUrl: episode.musicUrl,
        musicDescription: isRapBattle ? 'Rap battle soundtrack' : `Episode ${episode.episodeNumber} soundtrack`,
        audioDuration: episode.audioAnalysis?.duration?.toString(),
        characterProfileIds: characterIds,
        musicAnalysis: episode.audioAnalysis
          ? {
              bpm: episode.audioAnalysis.tempo,
              mood: episode.audioAnalysis.mood,
              structure: { sections: episode.audioAnalysis.sections },
              postProcessing: postProcessingOptions,
            }
          : postProcessingOptions
            ? { postProcessing: postProcessingOptions }
            : undefined,
      });

      const job = await storage.createJob(jobData);

      await storage.updateEpisode(episode.id, {
        jobId: job.id,
        status: 'generating',
      });

      res.json({
        success: true,
        data: { job, episode },
        message: `Video generation started for Episode ${episode.episodeNumber}`,
      });
    } catch (error: any) {
      console.error('Video generation error:', error);
      res.status(500).json({
        success: false,
        error: error.message || 'Failed to start video generation',
      });
    }
  });


