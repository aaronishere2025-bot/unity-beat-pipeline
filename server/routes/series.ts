  // ============ SERIES (Story Bible) ENDPOINTS ============

  // Create a new series from person, place, thing (or auto-discover from time period)
  router.post('/series/generate', authMiddleware, async (req, res) => {
    try {
      const { person, place, thing, genre, tone, timePeriod, isHistoricalFigure, additionalContext } = req.body;

      // Allow auto-discovery mode: historical figure + time period without person/place/thing
      const isAutoDiscovery = isHistoricalFigure && timePeriod && !person && !place && !thing;

      if (!isAutoDiscovery && (!person || !place || !thing)) {
        return res.status(400).json({
          success: false,
          error:
            'Person, place, and thing are required (or use Historical Figure mode with a time period for auto-discovery)',
        });
      }

      if (isAutoDiscovery) {
        console.log(`📚 Generating Story Bible (Auto-Discovery from ${timePeriod})...`);
      } else {
        console.log('📚 Generating Story Bible...');
      }

      const storyBible = await storyGenerator.generateStoryBible({
        person: person || '',
        place: place || '',
        thing: thing || '',
        genre,
        tone,
        timePeriod,
        isHistoricalFigure,
        additionalContext,
      });

      // Auto-generate lyrics for Episode 1 based on real historical events BEFORE creating series
      let autoGeneratedLyrics = null;
      const generatedSongs: Array<{
        episodeNumber: number;
        episodeTitle: string;
        style: string;
        lyrics: string;
        tags: string[];
        synopsis?: string;
        createdAt: string;
      }> = [];

      if (storyBible.episodeConcepts && storyBible.episodeConcepts.length > 0) {
        console.log('🎵 Auto-generating lyrics based on historical events...');
        try {
          const firstEpisode = storyBible.episodeConcepts[0];
          const musicPrompt = await storyGenerator.generateMusicPrompt(
            {
              title: storyBible.title,
              protagonist: storyBible.protagonist,
              antagonist: storyBible.antagonist,
              setting: storyBible.setting,
              storyArc: storyBible.storyArc,
              macguffin: storyBible.macguffin,
              supportingCharacters: storyBible.supportingCharacters,
              isHistorical: isHistoricalFigure,
              historicalNote: (storyBible as any).historicalNote,
            },
            1, // Episode 1
            {
              theme: 'origin' as const,
              conflict: firstEpisode.mainConflict || storyBible.storyArc.conflict,
              location: storyBible.setting.locations?.[0]?.name || storyBible.setting.name,
              emotionalJourney: firstEpisode.emotionalArc || 'doubt to determination',
              musicStyle: 'epic_orchestral' as const,
              tempo: 'medium' as const,
              mood: 'dramatic' as const,
            },
          );
          autoGeneratedLyrics = {
            episodeTitle: firstEpisode.title,
            sunoPrompt: musicPrompt.sunoPrompt,
            episodeContext: musicPrompt.episodeContext,
            recommendedDuration: musicPrompt.recommendedDuration,
          };

          // Store song in generatedSongs array
          generatedSongs.push({
            episodeNumber: 1,
            episodeTitle: firstEpisode.title,
            style: musicPrompt.sunoPrompt.style,
            lyrics: musicPrompt.sunoPrompt.lyrics,
            tags: musicPrompt.sunoPrompt.tags,
            synopsis: musicPrompt.episodeContext?.synopsis,
            createdAt: new Date().toISOString(),
          });

          console.log('   ✅ Auto-generated lyrics for Episode 1!');
        } catch (lyricsError) {
          console.error('   ⚠️ Failed to auto-generate lyrics:', lyricsError);
        }
      }

      const seriesData = insertSeriesSchema.parse({
        userId: req.user!.id,
        title: storyBible.title,
        protagonist: storyBible.protagonist,
        antagonist: storyBible.antagonist,
        setting: storyBible.setting,
        macguffin: storyBible.macguffin,
        storyArc: storyBible.storyArc,
        supportingCharacters: storyBible.supportingCharacters,
        visualStyle: storyBible.visualStyle,
        generatedSongs: generatedSongs.length > 0 ? generatedSongs : undefined,
      });

      let series = await storage.createSeries(seriesData);

      // Auto-generate character profiles for protagonist and antagonist
      const characterProfiles: { protagonist?: any; antagonist?: any } = {};

      try {
        console.log('🎨 Auto-generating character profile images...');

        const visualStyleDesc = storyBible.visualStyle?.cinematicStyle || 'cinematic portrait';

        const characterImages = await openaiService.generateCharacterImages(
          {
            name: storyBible.protagonist.name,
            description: storyBible.protagonist.description,
            traits: storyBible.protagonist.traits || [],
          },
          {
            name: storyBible.antagonist?.name || 'Antagonist',
            description: storyBible.antagonist?.description || 'A formidable adversary',
            traits: storyBible.antagonist?.traits || [],
          },
          visualStyleDesc,
        );

        // Save protagonist image and create profile
        const protagImageUrl = saveBase64Image(characterImages.protagonist.imageBase64, 'protagonist');
        const protagProfile = await storage.createCharacterProfile({
          name: storyBible.protagonist.name,
          refImageUrl: protagImageUrl,
          basePrompt: characterImages.protagonist.prompt,
          priority: 3.0, // Main character
        });
        characterProfiles.protagonist = protagProfile;
        console.log(`   ✅ Created protagonist profile: ${protagProfile.name} (${protagProfile.id})`);

        // Save antagonist image and create profile
        const antagImageUrl = saveBase64Image(characterImages.antagonist.imageBase64, 'antagonist');
        const antagProfile = await storage.createCharacterProfile({
          name: storyBible.antagonist?.name || 'Antagonist',
          refImageUrl: antagImageUrl,
          basePrompt: characterImages.antagonist.prompt,
          priority: 2.0, // Active side character
        });
        characterProfiles.antagonist = antagProfile;
        console.log(`   ✅ Created antagonist profile: ${antagProfile.name} (${antagProfile.id})`);

        // Update series with character profile IDs
        const updatedProtagonist = {
          ...storyBible.protagonist,
          characterProfileId: protagProfile.id,
        };
        const updatedAntagonist = {
          ...storyBible.antagonist,
          characterProfileId: antagProfile.id,
        };

        series =
          (await storage.updateSeries(series.id, {
            protagonist: updatedProtagonist,
            antagonist: updatedAntagonist,
          })) || series;

        console.log('✅ Character profiles linked to series!');
      } catch (charError: any) {
        console.error('⚠️ Failed to auto-generate character profiles:', charError.message);
        // Continue without character profiles - they can be added manually later
      }

      res.json({
        success: true,
        data: series,
        episodeConcepts: storyBible.episodeConcepts,
        autoGeneratedLyrics,
        characterProfiles: Object.keys(characterProfiles).length > 0 ? characterProfiles : undefined,
      });
    } catch (error: any) {
      console.error('Story generation error:', error);
      res.status(500).json({
        success: false,
        error: error.message || 'Failed to generate story bible',
      });
    }
  });


