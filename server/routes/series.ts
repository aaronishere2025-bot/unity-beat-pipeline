  // Get single series
  router.get('/series/:id', authMiddleware, async (req, res) => {
    try {
      const series = await storage.getSeries(req.params.id);
      if (!series) {
        return res.status(404).json({ success: false, error: 'Series not found' });
      }
      if ((series as any).userId && (series as any).userId !== req.user!.id) {
        return res.status(403).json({ success: false, error: 'Unauthorized' });
      }
      res.json({ success: true, data: series });
    } catch (error: any) {
      res.status(500).json({
        success: false,
        error: error.message || 'Failed to get series',
      });
    }
  });


  // Update series
  router.patch('/series/:id', authMiddleware, async (req, res) => {
    try {
      const existingSeries = await storage.getSeries(req.params.id);
      if (!existingSeries) {
        return res.status(404).json({ success: false, error: 'Series not found' });
      }
      if ((existingSeries as any).userId && (existingSeries as any).userId !== req.user!.id) {
        return res.status(403).json({ success: false, error: 'Unauthorized' });
      }
      const validatedData = updateSeriesSchema.parse(req.body);
      const series = await storage.updateSeries(req.params.id, validatedData);
      res.json({ success: true, data: series });
    } catch (error: any) {
      res.status(500).json({
        success: false,
        error: error.message || 'Failed to update series',
      });
    }
  });


